package com.solvd.onlinemarket.onmarket;

import java.time.LocalDateTime;
import java.util.List;

public class OnlineMarket {

    private Long id;
    private String name;
    private LocalDateTime foundedAtDate;
    private ContactInfo contactInfo;
    private List<Seller> sellers;
    private List<Customer> customers;
    private List<Problem> problems;
    private List<Review> reviews;
    private List<Product> products;
    private List<Employee> employees;
    private List<Order> orders;

    public OnlineMarket() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<Seller> getSellers() {
        return sellers;
    }

    public void setSellers(List<Seller> sellers) {
        this.sellers = sellers;
    }

    public List<Customer> getCustomers() {
        return customers;
    }

    public void setCustomers(List<Customer> customers) {
        this.customers = customers;
    }

    public List<Problem> getProblems() {
        return problems;
    }

    public void setProblems(List<Problem> problems) {
        this.problems = problems;
    }

    public List<Product> getProducts() {
        return products;
    }

    public void setProducts(List<Product> products) {
        this.products = products;
    }

    public List<Employee> getEmployees() {
        return employees;
    }

    public void setEmployees(List<Employee> employees) {
        this.employees = employees;
    }

    public ContactInfo getContactInfo() {
        return contactInfo;
    }

    public void setContactInfo(ContactInfo contactInfo) {
        this.contactInfo = contactInfo;
    }

    public LocalDateTime getFoundedAtDate() {
        return foundedAtDate;
    }

    public void setFoundedAtDate(LocalDateTime foundedAtDate) {
        this.foundedAtDate = foundedAtDate;
    }

    public List<Review> getReviews() {
        return reviews;
    }

    public void setReviews(List<Review> reviews) {
        this.reviews = reviews;
    }

    public List<Order> getOrders() {
        return orders;
    }

    public void setOrders(List<Order> orders) {
        this.orders = orders;
    }
}
